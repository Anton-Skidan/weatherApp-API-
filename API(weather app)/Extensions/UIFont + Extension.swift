import UIKit

extension UIFont {
    
    static func Helvetica17() -> UIFont? {
        return UIFont.init(name: "Helvetica", size: 17)
    }
    
    static func Helvetica14() -> UIFont? {
           return UIFont.init(name: "Helvetica", size: 14)
       }
    
    static func Helvetica30() -> UIFont? {
        return UIFont.init(name: "Helvetica", size: 30)
    }
    
    static func Kefa20() -> UIFont? {
        return UIFont.init(name: "Kefa", size: 20)
    }
    
    static func Kefa70() -> UIFont? {
        return UIFont.init(name: "Kefa", size: 70)
    }
}
